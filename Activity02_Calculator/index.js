//Recebimento de dados
let num1 = parseFloat(window.prompt('Insira um número:'))
let num2 = parseFloat(window.prompt('Insira outro número: '))

//Processamento de dados
let sum = num1 + num2
let sub = num1 - num2
let mult = num1 * num2
let div = num1 / num2

//Saída de dados
console.log('Adição: ' + sum)
console.log('Subtração: ' + sub)
console.log('Multiplicação: ' + mult)
console.log('Divisão: ' + div)